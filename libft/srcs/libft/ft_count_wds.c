#include "libft.h"

int	ft_count_wds(char const *s, char c)
{
	int	i;
	int	wrds;

	i = 0;
	wrds = 0;
	if (s[0] != c)
		wrds++;
	while (s[i + 1])
	{
		if ((s[i] == c) && (s[i + 1] != c))
			wrds++;
		i++;
	}
	return (wrds);
}
